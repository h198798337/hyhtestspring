	@Override
	public String createSeq(String name) {

		String sql = "select seq('" + name + "')";

		SQLQuery q = this.getCurrentSession().createSQLQuery(sql);

		return (String) (q.list().get(0));

	}

	/**
	 * 获取数据库表字段信息
	 * 
	 * @param tableName
	 *            表名
	 * @return
	 */
	@Override
	public List<TableCol> getTableColumns() {

		List<TableCol> talblColList = new LinkedList<TableCol>();
		Map<String, Object> params = new HashMap<String, Object>();
		String sql = "Select COLUMN_NAME, DATA_TYPE, COLUMN_COMMENT  from INFORMATION_SCHEMA.COLUMNS  Where table_name =:table_name";
		String tableName = "";
		for (Annotation annotation : entityClass.getAnnotations()) {
			if(annotation instanceof Table){
				Table table = (Table)annotation;
				tableName = table.name();
			}
		}
		params.put("table_name", tableName);
		List<Object[]> columnList = findBySql(sql, params);
		if (columnList != null && columnList.size() > 0) {
			for (int i = 0; i < columnList.size(); i++) {
				Object[] row = columnList.get(i);

				TableCol col = new TableCol();
				col.setColumnName(String.valueOf(row[0]));
				col.setDataType(String.valueOf(row[1]));
				col.setColumnComment(String.valueOf(row[2]));
				talblColList.add(col);
			}
		}

		return talblColList;

	}
